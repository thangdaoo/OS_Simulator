
public class Process {

		public String[] instructions;
		public int counter;
		public String state;
		public int ID;
		public String[] priority;
	
		public Process(int ID) {
			this.instructions = instructions;
			this.ID = ID;
			this.state = state;
			this.counter = counter;
	}
		
		public int returnID() {
			return ID;
		}
		
		public int counter() {
			return counter;
		}
		
		public String state() {
			return state;
		}
		
}
